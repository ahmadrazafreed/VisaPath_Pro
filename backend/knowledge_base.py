def build_system_prompt(country=None, visa_type=None):
    context = ""
    if country and visa_type and country in VISA_DB:
        d = VISA_DB[country]
        vt = visa_type.lower()
        if vt in d:
            v = d[vt]
            context = f"""
## ACTIVE CONTEXT: {country} {d['flag']} — {visa_type} Visa
Visa: {v['name']} | Cost: {v['cost']} | Processing: {v['time']}
Requirements: {" | ".join(v['requirements'])}
Process: {" → ".join(v['process'])}
Post-Visa: {v['post_visa']}
Tips: {v['tips']}
Official: {v.get('official_url','')}
"""

    kb = "\n## KNOWLEDGE BASE\n"
    for cn, cd in VISA_DB.items():
        kb += f"{cd['flag']} {cn}: "
        kb += " | ".join([f"{vt.title()} ({cd[vt]['name']}, {cd[vt]['time']}, {cd[vt]['cost']})" for vt in ['study','work','visit'] if vt in cd])
        kb += "\n"

    return f"""You are VisaPath AI — a world-class immigration consultant specializing in Canada, UK, USA, Germany, Australia, UAE, and New Zealand visas.

{context}
{kb}

## RESPONSE RULES — FOLLOW STRICTLY:

### For SIMPLE questions (greetings, yes/no, single fact):
- Answer in 1–3 sentences maximum
- No bullet points needed
- Be conversational and warm

### For NORMAL questions (what is X, how does X work, quick advice):
- 3–6 bullet points maximum
- Bold the key facts (costs, dates, scores)
- End with ONE clear next step
- Keep under 150 words

### For DETAILED questions (user says "explain in detail", "full guide", "step by step", "everything about"):
- Give comprehensive answer with sections
- Use headers and numbered steps
- Include costs, timelines, tips
- Can be up to 400 words

### ALWAYS:
- Match response length to question complexity
- Use **bold** for key numbers, costs, deadlines
- Mention Pakistan-specific requirements when relevant
- Be direct — no filler phrases like "Great question!" or "Certainly!"
- Only discuss visas, immigration, study/work abroad, language tests, relocation

### NEVER:
- Write essays when a sentence will do
- Repeat information already given in the conversation
- Use more than 3 sentences for simple questions
"""


def generate_chat_title(message: str) -> str:
    """Generate a smart short title from the first user message."""
    msg = message.strip().lower()

    countries = {
        "canada": "🇨🇦 Canada", "uk": "🇬🇧 UK", "united kingdom": "🇬🇧 UK",
        "england": "🇬🇧 UK", "britain": "🇬🇧 UK",
        "usa": "🇺🇸 USA", "america": "🇺🇸 USA", "united states": "🇺🇸 USA",
        "germany": "🇩🇪 Germany", "australia": "🇦🇺 Australia",
        "uae": "🇦🇪 UAE", "dubai": "🇦🇪 UAE", "emirates": "🇦🇪 UAE",
        "new zealand": "🇳🇿 New Zealand", "nz": "🇳🇿 New Zealand"
    }
    visas = {
        "study": "Study", "student": "Study", "f-1": "Study", "f1": "Study",
        "work": "Work", "worker": "Work", "h1b": "Work", "h-1b": "Work",
        "job": "Work", "employment": "Work", "lmia": "Work",
        "visit": "Visit", "tourist": "Visit", "visitor": "Visit", "tourism": "Visit",
        "pr": "PR", "permanent": "PR", "residency": "PR", "immigration": "Immigration",
        "schengen": "Schengen", "blue card": "EU Blue Card",
        "golden visa": "Golden Visa", "pgwp": "Post-Study Work",
    }

    found_country = next((v for k, v in countries.items() if k in msg), None)
    found_visa = next((v for k, v in visas.items() if k in msg), None)

    if found_country and found_visa:
        return f"{found_country} {found_visa} Visa"
    elif found_country:
        return f"{found_country} Visa"
    elif found_visa:
        return f"{found_visa} Visa Query"
    else:
        # Use first 5 meaningful words
        stop = {"what","how","can","is","are","i","do","the","a","an","me","my","for","about","tell"}
        words = [w.capitalize() for w in msg.split() if w not in stop][:4]
        return " ".join(words) if words else "New Chat"
